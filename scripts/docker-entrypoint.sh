#!/bin/bash
set -euo pipefail

# usage: file_env VAR [DEFAULT]
#    ie: file_env 'XYZ_DB_PASSWORD' 'example'
# (will allow for "$XYZ_DB_PASSWORD_FILE" to fill in the value of
#  "$XYZ_DB_PASSWORD" from a file, especially for Docker's secrets feature)
file_env() {
	local var="$1"
	local fileVar="${var}_FILE"
	local def="${2:-}"
	if [ "${!var:-}" ] && [ "${!fileVar:-}" ]; then
		echo >&2 "error: both $var and $fileVar are set (but are exclusive)"
		exit 1
	fi
	local val="$def"
	if [ "${!var:-}" ]; then
		val="${!var}"
	elif [ "${!fileVar:-}" ]; then
		val="$(< "${!fileVar}")"
	fi
	export "$var"="$val"
	unset "$fileVar"
}

file_env 'PRISMA_ENDPOINT'
file_env 'PRISMA_GAMEPLAY_ENDPOINT'
file_env 'PRISMA_MANAGEMENT_API_SECRET'
file_env 'JWT_SECRET'
file_env 'WS_REDIS_HOST'
file_env 'WS_REDIS_PORT'
file_env 'LOCK_REDIS_HOST'
file_env 'LOCK_REDIS_PORT'

exec "$@"
